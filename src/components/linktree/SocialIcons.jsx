function SocialIcon({ platformKey, iconColor, size }) {
  const icon = SOCIAL_ICONS[platformKey] || SOCIAL_ICONS.default
  
  return (
    <div style={{
      width: `${size}px`,
      height: `${size}px`,
      display: 'flex',
      alignItems: 'center',
      justifyContent: 'center',
      color: iconColor,
    }}>
      {icon}
    </div>
  )
}

export default function SocialIcons({ profile, theme, hoverZoom = 1, className = '', size = 28, gap = 16 }) {
  const iconColor = theme?.textColor || '#ffffff'
  
  const linkStyle = {
    display: 'inline-flex',
    transition: 'transform 0.2s ease',
  }

  // Get all active social links (that have URLs)
  const activeSocials = Object.entries(SOCIAL_PLATFORMS)
    .filter(([key]) => profile[key] && profile[key].trim() !== '')
  
  // No limit - show all active socials
  console.log('Active socials:', activeSocials.length, activeSocials.map(([k]) => k))

  if (activeSocials.length === 0) return null

  // Helper to format link based on platform
  const formatLink = (key, value) => {
    if (key === 'email') {
      // If already has mailto:, use as is, otherwise add it
      return value.startsWith('mailto:') ? value : `mailto:${value}`
    }
    if (key === 'phone') {
      // If already has tel:, use as is, otherwise add it
      return value.startsWith('tel:') ? value : `tel:${value.replace(/\s/g, '')}`
    }
    // For other platforms, use the URL as is
    return value
  }

  return (
    <div className={className} style={{ 
      display: 'flex', 
      gap: `${gap}px`, 
      margin: '24px 0 32px', 
      alignItems: 'center', 
      justifyContent: 'center',
      flexWrap: 'wrap',
    }}>
      {activeSocials.map(([key, platform]) => (
        <a 
          key={key}
          href={formatLink(key, profile[key])} 
          target={key === 'email' || key === 'phone' ? '_self' : '_blank'}
          rel="noopener noreferrer"
          style={linkStyle}
          onMouseEnter={e => e.currentTarget.style.transform = `scale(${hoverZoom})`}
          onMouseLeave={e => e.currentTarget.style.transform = 'scale(1)'}
          title={platform.name}
        >
          <SocialIcon platformKey={key} iconColor={iconColor} size={size} />
        </a>
      ))}
    </div>
  )
}
